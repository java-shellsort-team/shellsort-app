package team.shellsort.strategy;

/**
 * Перечисление доступных стратегий сортировки автомобилей.
 * Используется для выбора алгоритма сортировки в меню приложения.
 *
 * <ul>
 *   <li>{@link #BY_MODEL} — сортировка по названию модели (игнорируя регистр);</li>
 *   <li>{@link #BY_POWER} — сортировка по мощности двигателя;</li>
 *   <li>{@link #BY_YEAR} — сортировка по году выпуска.</li>
 * </ul>
 */
public enum SortKey {
    BY_MODEL,
    BY_POWER,
    BY_YEAR
}